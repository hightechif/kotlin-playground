fun main() {
    val x1 = 10 + 3  // 13
    val x2 = 10 - 3  // 7
    val x3 = 10 * 3  // 30
    val x4 = 10 / 3  // 3
    val x5 = 10f / 3 // 3.3333333
    val x6 = 10 % 3  // 1

    println("The value of x1 is $x1")
    println("The value of x2 is $x2")
    println("The value of x3 is $x3")
    println("The value of x4 is $x4")
    println("The value of x5 is $x5")
    println("The value of x6 is $x6")
}